	if tempVal!=0.:
		cp[3] += tempVal*1.0*(A2[2,1]) #20
		cp[4] += tempVal*2.0*(A1[2,1]*A1[2,2]) #11
		cp[5] += tempVal*1.0*(A2[2,2]) #2
	tempVal=c[6] # coef of 30
	if tempVal!=0.:
		cp[6] += tempVal*1.0*(A3[1,1]) #30
		cp[7] += tempVal*3.0*(A2[1,1]*A1[1,2]) #21
		cp[8] += tempVal*3.0*(A1[1,1]*A2[1,2]) #12
		cp[9] += tempVal*1.0*(A3[1,2]) #3
	tempVal=c[7] # coef of 21
