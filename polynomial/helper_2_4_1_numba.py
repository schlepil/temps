from numba import njit

@njit
def linCoordChange(c,cp, A0, A1, A2, A3, A4):

	tempVal=c[0] # coef of 0
	if tempVal!=0.:
		cp[0] += tempVal*1.0*(A1[0,0]) #0
	tempVal=c[1] # coef of 10
	if tempVal!=0.:
		cp[1] += tempVal*1.0*(A1[1,1]) #10
		cp[2] += tempVal*1.0*(A1[1,2]) #1
	tempVal=c[2] # coef of 1
	if tempVal!=0.:
		cp[1] += tempVal*1.0*(A1[2,1]) #10
		cp[2] += tempVal*1.0*(A1[2,2]) #1
	tempVal=c[3] # coef of 20
	if tempVal!=0.:
		cp[3] += tempVal*1.0*(A2[1,1]) #20
		cp[4] += tempVal*2.0*(A1[1,1]*A1[1,2]) #11
		cp[5] += tempVal*1.0*(A2[1,2]) #2
	tempVal=c[4] # coef of 11
	if tempVal!=0.:
		cp[3] += tempVal*1.0*(A1[1,1]*A1[2,1]) #20
		cp[4] += tempVal*1.0*(A1[1,2]*A1[2,1]) #11
		cp[4] += tempVal*1.0*(A1[1,1]*A1[2,2]) #11
		cp[5] += tempVal*1.0*(A1[1,2]*A1[2,2]) #2
